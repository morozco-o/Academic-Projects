  CREATE OR REPLACE EDITIONABLE PROCEDURE "ADJUST_LANGUAGE" (
    language_name IN VARCHAR2,
    chinese_check OUT BOOLEAN,
    error_message OUT VARCHAR2
) AS
BEGIN
    IF LOWER(language_name) = 'chinese' THEN
        chinese_check := TRUE;
