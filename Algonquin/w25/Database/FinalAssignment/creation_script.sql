  CREATE OR REPLACE EDITIONABLE TRIGGER "MOVIES_DETAILS_TRG" 
INSTEAD OF INSERT OR UPDATE OR DELETE ON MOVIES_DETAILS_VIEW
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO MOVIES (ID, NAME, YEAR, RATING, GENRE_ID, RUN_TIME, COUNTRY_ID, LANGUAGE_ID)
        VALUES (:NEW.ID, :NEW.NAME, :NEW.YEAR, :NEW.RATING, :NEW.GENRE_ID, :NEW.RUN_TIME, :NEW.COUNTRY_ID, :NEW.LANGUAGE_ID);
    ELSIF UPDATING THEN
        UPDATE MOVIES
        SET NAME = :NEW.NAME,
            YEAR = :NEW.YEAR,
            RATING = :NEW.RATING,
            GENRE_ID = :NEW.GENRE_ID,
            RUN_TIME = :NEW.RUN_TIME,
            COUNTRY_ID = :NEW.COUNTRY_ID,
            LANGUAGE_ID = :NEW.LANGUAGE_ID
        WHERE ID = :OLD.ID;
    ELSIF DELETING THEN
        DELETE FROM MOVIES
        WHERE ID = :OLD.ID;
    END IF;
END;

