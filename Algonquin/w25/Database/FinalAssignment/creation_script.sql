  CREATE OR REPLACE EDITIONABLE PROCEDURE "FAMILY_VALIDATION" (
    Genre_Type    IN VARCHAR2,
    is_Valid      OUT BOOLEAN,
    error_message OUT VARCHAR2
) AS
BEGIN
    IF REGEXP_LIKE(LOWER(Genre_Type), 'sex') THEN
        is_Valid := FALSE;
        error_message := 'This site is family friendly, adding adult movies not allowed';
    ELSIF REGEXP_LIKE(LOWER(Genre_Type), '\+18') THEN
        is_Valid := FALSE;
        error_message := 'This site is family friendly, adding adult movies not allowed';
    ELSIF REGEXP_LIKE(LOWER(Genre_Type), 'adult') THEN
        is_Valid := FALSE;
        error_message := 'This site is family friendly, adding adult movies not allowed';
    ELSE
        is_Valid := TRUE;
        error_message := NULL;
    END IF;
END;


